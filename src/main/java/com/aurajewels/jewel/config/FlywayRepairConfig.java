package com.aurajewels.jewel.config;

import org.springframework.boot.autoconfigure.flyway.FlywayMigrationStrategy;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

/**
 * Flyway configuration that auto-repairs failed migrations before running migrate.
 *
 * <p>This handles the case where a previous migration partially failed (e.g., V6 ALTER TABLE on
 * columns that already existed), leaving a failed entry in flyway_schema_history. Without repair,
 * Flyway refuses to run any further migrations.
 *
 * <p>The strategy calls {@code flyway.repair()} first (removes failed entries from history), then
 * {@code flyway.migrate()} to apply pending migrations.
 *
 * @author Raviraj Bhosale
 */
@Configuration
public class FlywayRepairConfig {

    @Bean
    public FlywayMigrationStrategy flywayMigrationStrategy() {
        return flyway -> {
            flyway.repair();
            flyway.migrate();
        };
    }
}
