package com.aurajewels.jewel.exception;

/**
 * Thrown when a request targets a feature module that is not enabled for the current store.
 *
 * @author Raviraj Bhosale
 */
public class ModuleNotEnabledException extends RuntimeException {

    private final String module;

    public ModuleNotEnabledException(String module) {
        super("Module not enabled for this store: " + module);
        this.module = module;
    }

    public String getModule() {
        return module;
    }
}
